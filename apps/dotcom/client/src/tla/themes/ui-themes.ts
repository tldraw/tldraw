/**
 * Derive a `TLTheme` from a UI theme by mapping CSS variable values onto the
 * canvas-facing fields of `TLThemeColors`. Shape palette colors (red, blue,
 * etc.) are inherited from `DEFAULT_THEME` so existing drawings keep their
 * colors when the theme changes.
 */
function buildTLTheme(def: UITheme): TLTheme {
	const map = (tl: Record<string, string>, fallback: TLTheme['colors']['light']) => ({
		...fallback,
		text: tl['tl-color-text'] ?? fallback.text,
		background: tl['tl-color-background'] ?? fallback.background,
		negativeSpace: tl['tl-color-background'] ?? fallback.negativeSpace,
		selectionStroke:
			tl['tl-color-selection-stroke'] ?? tl['tl-color-selected'] ?? fallback.selectionStroke,
		selectionFill: tl['tl-color-selection-fill'] ?? fallback.selectionFill,
		selectedContrast: tl['tl-color-selected-contrast'] ?? fallback.selectedContrast,
		brushFill: tl['tl-color-brush-fill'] ?? fallback.brushFill,
		brushStroke: tl['tl-color-brush-stroke'] ?? fallback.brushStroke,
		snap: tl['tl-color-snap'] ?? fallback.snap,
		laser: tl['tl-color-laser'] ?? fallback.laser,
	})

	return {
		...DEFAULT_THEME,
		id: def.id as TLTheme['id'],
		colors: {
			light: map(def.light.tl, DEFAULT_THEME.colors.light),
			dark: map(def.dark.tl, DEFAULT_THEME.colors.dark),
		},
	}
}

export const UI_THEMES: Array<UITheme & { theme: TLTheme }> = THEMES.map((def) => ({
	...def,
	theme: buildTLTheme(def),
}))
