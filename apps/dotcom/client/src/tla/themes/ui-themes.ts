/** CSS variable overrides for a single light or dark variant */
interface TlaUIThemeVariant {
	tl: Record<string, string>
	tla: Record<string, string>
}

/** A complete UI color theme with both light and dark variants */
export interface TlaUITheme {
	id: string
	name: string
	light: TlaUIThemeVariant
	dark: TlaUIThemeVariant
	lightBackground: string
	darkBackground: string
}

/** Metadata for each available theme (no CSS values, used for the picker UI) */
export interface TlaUIThemeEntry {
	id: string
	name: string
	lightBackground: string
	darkBackground: string
}

